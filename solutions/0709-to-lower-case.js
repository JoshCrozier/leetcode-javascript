/**
 * 709. To Lower Case
 * https://leetcode.com/problems/to-lower-case/
 * Difficulty: Easy
 *
 * Given a string s, return the string after replacing every uppercase letter with the same
 * lowercase letter.
 */

/**
 * @param {string} s
 * @return {string}
 */
var toLowerCase = function(s) {
  return s.toLowerCase();
};
